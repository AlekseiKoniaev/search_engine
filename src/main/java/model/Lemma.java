package model;

public class Lemma {
    
    private String lemma;
    private int frequency;
    
    public String getLemma() {
        return lemma;
    }
    
    public void setLemma(String lemma) {
        this.lemma = lemma;
    }
    
    public int getFrequency() {
        return frequency;
    }
    
    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }
}
